# Entry — called from server.py CLI subparser
# ---------------------------------------------------------------------------


def cmd_init(args: argparse.Namespace) -> int:
    """``plantsim-copilot-mcp init`` entry point."""
    target_cfg = Path(args.config).expanduser().resolve() if args.config else (
        agent_home() / CONFIG_FILENAME
    )

    if args.non_interactive:
        ans = _build_non_interactive(args)
    else:
        if target_cfg.exists() and not args.force:
            print(f"Config already exists at {target_cfg}.")
            if not _prompt_yes_no("Overwrite?", default=False):
                print("Aborted.")
                return 1
        ans = run_interactive()

    print_summary(ans)

    if not args.non_interactive:
        confirm = _prompt_yes_no(f"\nWrite to {target_cfg}?", default=True)
        if not confirm:
            print("Aborted; nothing written.")
            return 1

    write_config(ans, target_cfg)
    print(f"\nWrote {target_cfg}")

    # Build step
    if args.non_interactive:
        run_build = args.build
    else:
        run_build = _prompt_yes_no(
            "\nBuild indices now? (uses the paths above)", default=True
        )
    fails = maybe_build(ans, run_build=run_build)

    if fails == 0:
        print("\nDone. Start the server with:")
        print("  plantsim-copilot-mcp serve")
    else:
        print(f"\nFinished with {fails} build error(s) — see messages above.")
    return 0 if fails == 0 else 1


def _build_non_interactive(args: argparse.Namespace) -> WizardAnswers:
    """Materialise WizardAnswers from CLI flags for scripted / test use."""
    ans = WizardAnswers()
    if args.kb_root:
        for r in args.kb_root:
            p = Path(r).expanduser().resolve()
            if not p.is_dir():
                raise SystemExit(f"--kb-root not a directory: {p}")
            ans.help_kb_roots.append(p)
    if args.fullmd_src:
        p = Path(args.fullmd_src).expanduser().resolve()
        if not p.is_file():
            raise SystemExit(f"--fullmd-src not a file: {p}")
        ans.fullmd_src = p
    if args.chapters:
        ans.fullmd_chapters = list(args.chapters)
    if args.project:
        p = Path(args.project).expanduser().resolve()
        if not p.is_dir():
            raise SystemExit(f"--project not a directory: {p}")
        ans.default_project = p
    if args.index_dir:
        ans.index_dir = Path(args.index_dir).expanduser().resolve()
    return ans


def add_init_subparser(subparsers: argparse._SubParsersAction) -> None:
    """Register the ``init`` subcommand on a parent ``argparse`` subparsers."""
    p = subparsers.add_parser(
        "init",
        help="interactive setup wizard — writes ~/.plantsim-agent/config.toml",
    )
    p.add_argument(
        "--config",
        default=None,
        help="write config to this path (default: ~/.plantsim-agent/config.toml)",
    )
    p.add_argument(
        "--force",
        action="store_true",
        help="overwrite an existing config without prompting",
    )
    p.add_argument(
        "--non-interactive",
        action="store_true",
        help="don't prompt — take everything from --kb-root / --fullmd-src / ... flags",
    )
    # Non-interactive flags (also harmless in interactive mode).
    p.add_argument(
        "--kb-root",
        action="append",
        default=None,
        help="markdown KB root (repeat). Required in --non-interactive mode if "
        "no other source given.",
    )
    p.add_argument(
        "--fullmd-src",
        default=None,
        help="path to PTS Help fullmd file (single .md)",
    )
    p.add_argument(
        "--chapters",
        type=lambda s: [int(p) for p in s.replace(",", " ").split() if p.strip()],
        default=None,
        help='chapters to index from --fullmd-src (e.g. "11,12,13,15")',
    )
    p.add_argument(
        "--project",
        default=None,
        help="default .psfm folder for find_method/find_callers/etc.",
    )
    p.add_argument(
        "--index-dir",
        default=None,
        help="where help.db / project.db are written (default: $PLANTSIM_AGENT_HOME/indices)",
    )
    p.add_argument(
        "--build",
        action="store_true",
        help="(--non-interactive only) run the indexers after writing config",
    )
